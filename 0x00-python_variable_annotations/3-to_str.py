#!/usr/bin/env/ python3
'''a type-annotated function to_str that takes a float n as argument and returns the string
representation of the float.'''


def to_str(n: float) -> str:
    """
    Return the string of a float.

    Args:
        n (float): The input float.

    Returns:
        str: The string of the input float.
    """
    return str(n)
